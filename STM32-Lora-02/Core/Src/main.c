// Read 4 channels PA1-PA4
void Read_ADC_Channels(void)
{
    ADC_ChannelConfTypeDef sConfig = {0};

    for (uint8_t i = 0; i < 4; i++) {
        sConfig.Channel = ADC_CHANNEL_1 + i;   
        sConfig.Rank = 1;
        sConfig.SamplingTime = ADC_SAMPLETIME_55CYCLES_5;
        HAL_ADC_ConfigChannel(&hadc1, &sConfig);

        HAL_ADC_Start(&hadc1);
        if (HAL_ADC_PollForConversion(&hadc1, 10) == HAL_OK) {
            adc_values[i] = HAL_ADC_GetValue(&hadc1);
        }
        HAL_ADC_Stop(&hadc1);
    }
}

void Transmit_ADC_Values(void)
{
    Read_ADC_Channels();
    snprintf(send_data, sizeof(send_data), 
             "A1:%u A2:%u A3:%u A4:%u",
             adc_values[0], adc_values[1], adc_values[2], adc_values[3]);


    LoRa_setSyncWord(&myLoRa, 0x3A);
    if (LoRa_transmit(&myLoRa, (uint8_t*)send_data, strlen(send_data), 100) == 1) {
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);  // Blink LED if sent
    }
}

